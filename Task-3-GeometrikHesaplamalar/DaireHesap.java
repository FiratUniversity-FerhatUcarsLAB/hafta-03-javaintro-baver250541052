public class DairelHesap {
    public static void main(String[] args) {
        double r = 5.5;
        double pi = 3.14159;


        //  Alan Hesapla

        double alan = pi * r * r ;

        // Cevre Hesapla
        double cevre = 2 * pi * r ;

        // Sonuclari Yazdir
        System.out.println("Dairenin Alani: " + alan);
        System.out.println("Dairenin Cevresi: " + cevre);
    }



    
}
